// String to Hexadecimal
export function toHex(str: string) {
  // Set Variables
  let result: string = ''
  // Iterate over String Char
  str.split('').forEach(v => {
    const char = v.charCodeAt(0).toString(16)
    result += `${char.length < 2 ? '0' : ''}${char}`
  })
  // Return Hex String
  return result
}

