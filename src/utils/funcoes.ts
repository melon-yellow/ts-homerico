// String to Hexadecimal
export function toHex(str: string) {
  // Set Variables
  let result: string = ''
  // Iterate over String Char
  str.split('').forEach(v => {
    result += v.charCodeAt(0).toString(16)
  })
  // Return Hex String
  return result
}

